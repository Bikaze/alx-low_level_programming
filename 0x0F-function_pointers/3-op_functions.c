/**
  *op_add - function to add two numbers
  *@a: first number
  *@b: second number
  *Return: integer
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  *op_sub - function return difference between two numbers
  *@a: first number
  *@b: second number
  *Return: integer
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  *op_mul - function to multiply two numbers
  *@a: first number
  *@b: second number
  *Return: integer
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  *op_div - function to divide two numbers
  *@a: first number
  *@b: second number
  *Return: integer
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  *op_mod - function to modulate two numbers
  *@a: first number
  *@b: second number
  *Return: integer
  */

int op_mod(int a, int b)
{
	return (a % b);
}
